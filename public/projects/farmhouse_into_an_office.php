<h1 class="hidden">Farmhouse Into an Office</h1>

<p>When I first arrived we were looking at an OLD farmhouse on a great piece of property...but Ron wanted his law offices in this house.</p>

<p>We quickly retrofitted modern supports in the cellar to support all our modern updates and to insure a solid project.</p>

<p>After meeting the challenges of having all the floors join correctly, getting updated plumbing, heating and some electrical we implemented the process of beautifying through built in bookcases, tile and wood floors and three tone paint.</p>

<p>The farm was now a three office, conference room, reception area and bath...</p>

<p>Challenging? yes! Fulfilling to see it succeed? More so!</p>

