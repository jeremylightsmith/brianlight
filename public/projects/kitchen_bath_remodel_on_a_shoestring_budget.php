<p>The work had to be put together saving as much as possible at every step.</p>

<p>The client mail ordered these cabinets in and we assembled them on sight.</p>

<p>Granite countertops were preferred, so we ordered some granite 12x12 tiles online and built the countertops.</p>

<p>With upgraded electrical needs came a second electric panel and we worked a new washer dryer hook up into the kitchen area as well (few of these condos have a W/D).</p>

<p>Time was of an essence! From start to finish we had three weeks and the bathroom was included as an after thought. We also replaced closet doors and trim during this time, installed travertine stone floors, and repaired drywall from an upstairs leak that occurred the week before we started.</p>

<p>On time and on budget!</p>

